const DEFAULT_THEME_KEY = "software";

function getSlug(pathname = "") {
  const cleaned = pathname.split("?")[0].split("#")[0];
  const parts = cleaned.split("/").filter(Boolean);
  return parts[parts.length - 1] || "";
}

function resolveThemeKey(slug) {
  if (!slug) return DEFAULT_THEME_KEY;

  if (slug.includes("seo")) return "seo";
  if (slug.includes("smo")) return "seo";
  if (slug.includes("content-marketing")) return "seo";

  if (slug.includes("devops")) return "devops";

  if (slug.includes("ai")) return "ai";
  if (slug.includes("machine-learning")) return "ai";
  if (slug.includes("rpa")) return "ai";

  if (slug.includes("mobile") || slug.includes("app")) return "mobile";
  if (slug.includes("api") || slug.includes("integration")) return "api";
  if (slug.includes("testing") || slug.includes("support-maintenance")) return "qa";
  if (slug.includes("consulting") || slug.includes("auditing") || slug.includes("outsourcing")) {
    return "consulting";
  }

  return "software";
}

export function getCategoryPageContent(pathname = "") {
  const slug = getSlug(pathname);
  const themeKey = resolveThemeKey(slug);
  return THEME_CONTENT[themeKey] || THEME_CONTENT[DEFAULT_THEME_KEY];
}

